	private String language;
	private String officialLanguage;
	
	MinecraftLocale(String language, String officialLanguage) {
		this.language = language;
		this.officialLanguage = officialLanguage;
	}
	
	public String getLanguage() {
		return language;
	}
	public String getOfficialLanguage() {
		return officialLanguage;
	}
	public String getCode() {
		return name().toLowerCase();
	}
	
	public static MinecraftLocale getByString(String value) {
		for(MinecraftLocale locale : MinecraftLocale.values()) if(locale.name().equalsIgnoreCase(value) || locale.getLanguage().equalsIgnoreCase(value) || locale.getOfficialLanguage().equalsIgnoreCase(value)) return locale;
		return null;
	}
	public static MinecraftLocale getDefault() {
		return MinecraftLocale.EN_US;
	}
